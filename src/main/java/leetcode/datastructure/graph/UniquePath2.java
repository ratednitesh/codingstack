    // Approach 1: Create a cache memory.
    // Once found the path to end point, update the cache memory of each cell in the path
    // to the numberOfPaths found till that point.
    // Next time if cache memory is set to other than zero, directly
    // return from there with cache value
    public static int uniquePathsWithObstacles(int[][] obstacleGrid) {
        // edge cases
        if (obstacleGrid[0][0] == 1)
            return 0;
        int m = obstacleGrid.length, n = obstacleGrid[0].length;
        if (m == 1 && n == 1)
            return 1;
        int[][] isVisited = new int[m][n];
        return dfs(obstacleGrid, m, n, isVisited, 0, 0);
    }

    public static int dfs(int[][] grid, int m, int n, int[][] isVisited, int i, int j) {

        if (i >= m || j >= n || grid[i][j] == 1)
            return 0;
        if (isVisited[i][j] != 0)
            return isVisited[i][j];
        if (i == m - 1 && j == n - 1)
            return 1;
        else {
            int a = dfs(grid, m, n, isVisited, i + 1, j) +
                    dfs(grid, m, n, isVisited, i, j + 1);

            isVisited[i][j] = a;
            return a;
        }
    }

    // Approach 2: Optimal Approach using DP.
    public static int uniquePathsWithObstaclesDP(int[][] obstacleGrid) {
        if (obstacleGrid[0][0] == 1) // edge case
            return 0;
        int m = obstacleGrid.length, n = obstacleGrid[0].length;
        if (m == 1 && n == 1) // edge case
            return 1;
        int[][] cache = new int[m][n]; // create a cache matrix to store path so far.
        for (int i = 0; i < m; i++) {
            if (obstacleGrid[i][0] == 0)
                cache[i][0] = 1;  // initialize cache cell for first column as long as you come across first '1' in grid.
            else
                break;// once you meet the 1st obstacle no need to iterate further.
        }
        for (int j = 0; j < n; j++) { // Similar logic for first row..
            if (obstacleGrid[0][j] == 0)
                cache[0][j] = 1;
            else
                break;
        }
        for (int i = 1; i < m; i++) { // compute the cache for each cell based on previous cells.
            for (int j = 1; j < n; j++) {
                if (obstacleGrid[i][j] == 0) // in case grid[i][j] == 1, it is an obstacle no need to calculate cache as it will be '0'
                    cache[i][j] = cache[i - 1][j] + cache[i][j - 1];
            }
        }
        return cache[m - 1][n - 1]; // return value of last cell in the cache array.
    }
